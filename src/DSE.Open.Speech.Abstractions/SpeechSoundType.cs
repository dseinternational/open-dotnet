namespace DSE.Open.Speech;

public enum SpeechSoundType
{
    /// <summary>
    /// A speech sound where breath is at least partly obstructed and which can be combined
    /// with a vowel to form a syllable
    /// </summary>
    Consonant,

    /// <summary>
    /// A speech sound where the breath flows out through the mouth without being blocked by
    /// the teeth, tongue, or lips
    /// </summary>
    Vowel,
}
